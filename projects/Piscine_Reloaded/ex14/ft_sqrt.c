int		ft_sqrt(int nb)
{
	int i;
	int sq;

	i = 1;
	sq = 0;
	if (nb <= 2147395600)
		while ((sq = i * i) < nb)
			i++;
	if (sq == nb)
		return (i);
	return (0);
}
